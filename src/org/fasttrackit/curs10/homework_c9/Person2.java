package org.fasttrackit.curs10.homework_c9;

public class Person2 {
    private String name;
    private int age;
    private String position;

    public Person2(String name, int age, String position) {
        this.name = name;
        this.age = age;
        this.position = position;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getPosition() {
        return position;
    }
}

